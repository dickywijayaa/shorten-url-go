package helpers

import (
	"math/rand"
)

type Helper struct {
}

func HelperHandler() Helper {
	return Helper{}
}

func (h *Helper) GenerateRandomString(n int) string {
	var letters = []rune("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_")

	b := make([]rune, n)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	return string(b)
}